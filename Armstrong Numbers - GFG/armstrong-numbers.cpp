class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int X = n;
        int arm = 0;
        while(X != 0){
            int digit = X%10;
            arm += digit*digit*digit;
            X = X/10;
        }
        
        if(n == arm){
            return "Yes";
        }
        return "No";
    }
};

